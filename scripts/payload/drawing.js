// actual canvas drawing happens here
function draw (ctx) {

    ctx.fillRect(

      // left top corner
      ctx.canvas.width / 4,
      ctx.canvas.height / 4,

      // width and height
      ctx.canvas.width / 2,
      ctx.canvas.height / 2,
    )

}
